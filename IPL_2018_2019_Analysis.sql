/* THE NAMES AND TEAMS OF ALL ALLROUNDERS IN THE YEAR 2019 */

SELECT p.Player, p.Team FROM `men_IPL.2019_Batsmen` AS p INNER JOIN `men_IPL.2019_Bowlers` AS a ON p.Player = a.Player;

/* THE LIST OF BATSMEN WHO PLAYED IN BOTH 2018 AND 2019 SEASONS */

SELECT a.Player FROM `men_IPL.2019_Batsmen` AS a INNER JOIN `men_IPL.2018_Batsmen` AS b ON a.Player = b.Player;

